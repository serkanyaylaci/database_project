
CREATE TABLE musteri (
    musteri_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ad VARCHAR2(50),
    soyad VARCHAR2(50),
    email VARCHAR2(100),
    telefon VARCHAR2(20)
);


CREATE TABLE oda_tipleri (
    oda_tipi_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    oda_tipi_adi VARCHAR2(20)
);


CREATE TABLE oda (
    oda_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    oda_numarasi VARCHAR2(10),
    oda_tipi_id NUMBER, 
    fiyat NUMBER(10,2) CHECK (fiyat > 0), 
    FOREIGN KEY (oda_tipi_id) REFERENCES oda_tipleri(oda_tipi_id)
);


CREATE TABLE personel (
    personel_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ad VARCHAR2(30),
    soyad VARCHAR2(20),
    calistigi_birim VARCHAR2(50),
    aldigi_maas NUMBER(10,2)
);


CREATE TABLE etkinlikler (
    etkinlik_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    etkinlik_adi VARCHAR2(50),
    etkinlik_tarihi DATE,
    etkinlik_saati VARCHAR2(5) 
);


CREATE TABLE rezervasyon_tipi (
    rezervasyon_tipi_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    rezervasyon_tipi_adi VARCHAR2(20)
);


CREATE TABLE rezervasyon (
    rezervasyon_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    musteri_id NUMBER,
    oda_id NUMBER,
    rezervasyon_tipi_id NUMBER, 
    rezervasyon_tarihi DATE,
    check_in_tarihi DATE,
    check_out_tarihi DATE,
    toplam_ucret NUMBER(10,2),
    FOREIGN KEY (musteri_id) REFERENCES musteri(musteri_id),
    FOREIGN KEY (oda_id) REFERENCES oda(oda_id),
    FOREIGN KEY (rezervasyon_tipi_id) REFERENCES rezervasyon_tipi(rezervasyon_tipi_id)
);


CREATE TABLE etkinlik_katilimcilar (
    etkinlik_id NUMBER,
    musteri_id NUMBER,
    PRIMARY KEY (etkinlik_id, musteri_id),
    FOREIGN KEY (etkinlik_id) REFERENCES etkinlikler(etkinlik_id),
    FOREIGN KEY (musteri_id) REFERENCES musteri(musteri_id)
);


CREATE TABLE personelin_yaptigi_is (
    personel_id NUMBER,
    yaptigi_is VARCHAR2(100),
    PRIMARY KEY (personel_id, yaptigi_is),
    FOREIGN KEY (personel_id) REFERENCES personel(personel_id) 
);
CREATE TABLE ucret (
    ucret_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    rezervasyon_id NUMBER,
    odeme_tarihi DATE,
    odeme_miktari NUMBER(10,2),
    odeme_yontemi VARCHAR2(20),
    FOREIGN KEY (rezervasyon_id) REFERENCES rezervasyon(rezervasyon_id)
);
